<template>
  <v-row justify="center">
    <v-dialog v-model="dialog = active" persistent max-width="600px">
      <template v-slot:activator="{ on, attrs }">
       
      </template>
      <v-card>
        <v-card-title>
          <span class="headline">User Profile</span>
        </v-card-title>
        <v-card-text>
          <v-container>
            <v-row>
              <v-col cols="12" sm="6" md="4">
                <v-text-field label="name" :value="user.name" required></v-text-field>
              </v-col>
              <v-col cols="12">
                <v-text-field label="Email*" :value="user.email" required></v-text-field>
              </v-col>
              <v-col cols="12">
                <v-text-field label="Password*" :value="user.password" type="password" required></v-text-field>
              </v-col>
            </v-row>
          </v-container>
        </v-card-text>
        <v-card-actions>
          <v-spacer></v-spacer>
          <v-btn color="blue darken-1" text @click="closeDialog()">Close</v-btn>
          <v-btn color="blue darken-1" text @click="dialog = false">Save</v-btn>
        </v-card-actions>
      </v-card>
    </v-dialog>
  </v-row>
</template>

<script>
  export default {
    data () {
        return {
            dialog: false,
        }
    },
    methods: {
          closeDialog(){
            this.$store.dispatch('closeDialog')
          },
    },
    computed: {
        active(){
            return this.$store.getters.activeDialog
        },
        user(){
          return this.$store.state.users
        }
    }
  }
</script>